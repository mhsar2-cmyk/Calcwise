class App {
  constructor() {
    this.currentLang = localStorage.getItem("lang") || "en";
    this.currentTheme = localStorage.getItem("theme") || "dark";
    this.init();
  }

  init() {
    this.setupEventListeners();
    this.applyTheme(this.currentTheme);
    this.applyLanguage(this.currentLang);
    this.setupDropdowns();
    this.setupScrollReveal();
    this.setupBackToTop();
  }

  setupBackToTop() {
    const backBtn = document.getElementById('back-to-top');
    if (!backBtn) return;

    window.addEventListener('scroll', () => {
      if (window.scrollY > 500) {
        backBtn.classList.add('visible');
      } else {
        backBtn.classList.remove('visible');
      }
    });

    backBtn.addEventListener('click', () => {
      window.scrollTo({
        top: 0,
        behavior: 'smooth'
      });
    });
  }

  setupScrollReveal() {
    const observerOptions = {
      threshold: 0.15,
      rootMargin: "0px 0px -50px 0px"
    };

    const observer = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('revealed');
          observer.unobserve(entry.target);
        }
      });
    }, observerOptions);

    document.querySelectorAll('[data-reveal]').forEach(el => observer.observe(el));
  }

  setupEventListeners() {
    // Language Toggle
    const langBtn = document.getElementById("lang-toggle");
    if (langBtn) {
      langBtn.addEventListener("click", () => {
        const nextLang = this.currentLang === "en" ? "ar" : "en";
        this.applyLanguage(nextLang);
      });
    }

    // Theme Toggle
    const themeBtn = document.getElementById("theme-toggle");
    if (themeBtn) {
      themeBtn.addEventListener("click", () => {
        const nextTheme = this.currentTheme === "dark" ? "light" : "dark";
        this.applyTheme(nextTheme);
      });
    }

    // Mobile Toggle
    const mobileBtn = document.getElementById("mobile-toggle");
    const navLinks = document.querySelector(".nav-links");
    if (mobileBtn && navLinks) {
      mobileBtn.addEventListener("click", () => {
        navLinks.classList.toggle("active");
      });
    }

    // Smooth Scrolling for nav links
    document.querySelectorAll('.nav-link[href^="#"]').forEach(anchor => {
      anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
          target.scrollIntoView({
            behavior: 'smooth'
          });
        }
      });
    });
  }

  applyTheme(theme) {
    this.currentTheme = theme;
    document.documentElement.setAttribute("data-theme", theme);
    localStorage.setItem("theme", theme);

    const sunIcon = document.querySelector(".sun-icon");
    const moonIcon = document.querySelector(".moon-icon");
    if (sunIcon && moonIcon) {
      sunIcon.style.display = theme === "dark" ? "block" : "none";
      moonIcon.style.display = theme === "dark" ? "none" : "block";
    }
  }

  applyLanguage(lang) {
    this.currentLang = lang;
    const html = document.documentElement;
    html.setAttribute("lang", lang);
    html.setAttribute("dir", lang === "ar" ? "rtl" : "ltr");
    localStorage.setItem("lang", lang);

    const langText = document.querySelector(".lang-text");
    if (langText) langText.innerText = lang.toUpperCase();

    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (translations[lang] && translations[lang][key]) {
        el.innerText = translations[lang][key];
      }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      if (translations[lang] && translations[lang][key]) {
        el.placeholder = translations[lang][key];
      }
    });
  }

  setupDropdowns() {
    const dropdowns = document.querySelectorAll('.dropdown');

    // Toggle on click
    dropdowns.forEach(dropdown => {
      const toggle = dropdown.querySelector('.dropdown-toggle');
      if (toggle) {
        toggle.addEventListener('click', (e) => {
          // On mobile, always toggle. On desktop, toggle if it's a touch or explicit click
          if (window.innerWidth <= 1200 || ('ontouchstart' in window)) {
            e.preventDefault();
            e.stopPropagation();

            // Close other dropdowns first
            dropdowns.forEach(other => {
              if (other !== dropdown) other.classList.remove('active');
            });

            dropdown.classList.toggle('active');
          }
        });
      }
    });

    // Close on click outside
    document.addEventListener('click', () => {
      dropdowns.forEach(dropdown => dropdown.classList.remove('active'));
    });

    // Prevent closing when clicking inside the menu
    document.querySelectorAll('.dropdown-menu').forEach(menu => {
      menu.addEventListener('click', (e) => e.stopPropagation());
    });
  }
}

document.addEventListener("DOMContentLoaded", () => {
  new App();
});
